//            ------- Calculadora simples c++ ---------

void somar(int a,int b,int& soma,int sub, int div){
soma = a + b;
}

void subt(int a,int b,int soma,int& sub, int div){
sub = a - b;
}

void divi(int a,int b,int soma,int sub, int& div){
div = a / b;
}

int main()
{
    int a,b,soma,sub,div;
    int ope;

   std::cout << "--------CALCULADORA BASICA C++----------" << std::endl;
   std:: cout << "          " << std::endl;

   std::cout << "Digite o primeiro numero: ";
   std::cin >> a;

   std::cout <<"Digite o segundo numero: ";
   std::cin >> b;

   std::cout << "Escolha a operacao (1-soma       2-subtracao           3-divisao): ";
   std::cin >> ope;

   switch(ope){

   case 1:
    somar(a,b,soma,sub,div);
    std::cout << a << "+" << b << " = " << soma << std::endl;
    break;

    case 2:
    subt(a,b,soma,sub,div);
    std::cout << a << "-" << b << " = " << sub << std::endl;
    break;

    case 3:
    divi(a,b,soma,sub,div);
    std::cout << a << "/" << b << " = " << div;
    break;





   }

   return 0;

}
